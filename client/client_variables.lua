SELLERSTATS = {
    state = false
}

---@param text string Notification text
---@param nType string Notification type: 'success' | 'error' | 'inform' | 'warning'
SELLERSTATS.notify = function(text, nType)
    local provider = Config.NotificationProvider or 'ox_lib'
    if provider == 'ox_lib' then
        local oxType = nType == 'error' and 'error'
                    or nType == 'success' and 'success'
                    or nType == 'warning' and 'warning'
                    or 'inform'
        exports.ox_lib:notify({ type = oxType, description = text })
    elseif provider == 'vorp' then
        TriggerEvent('vorp:TipRight', text, 4000)
    else
        -- fallback: print to console
        print('[lxr-artheist] ' .. tostring(text))
    end
end